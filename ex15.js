//Criando switch case

let numero = Number(prompt('Qual número é: '));

switch(numero)
{
    case 0:
    console.log('O número que voce escolheu é zero!');
    break;

    case 1:
    console.log('O número que voce escolheu é 1! é um número impar');
    break;

    case 2:
    console.log('O número que voce escolheu é 2! que também é um número par!');
}
